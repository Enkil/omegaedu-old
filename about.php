<p style="font-size:21px;">Чем занимается образовательный проект «Омега»?</p>

<p>Омега создана с целью помогать школьникам готовиться и поступать в ведущие вузы России. Раньше все участники нашей команды занимались проблемами образования по отдельности, и вот уже примерно год как мы вместе. Чем мы занимаемся? Мы делаем крутой образовательный контент и с помощью собственных методик успешно запихиваем его вам в голову. Мы верим, что каждый ученик при достаточном упорстве может подготовится к сдаче экзаменов на высокий балл, достаточный для поступления в ведущие вузы нашей родины, такие как МФТИ, МГУ, МИФИ, МГТУ им. Баумана и другие, и обеспечить себе таким образом хорошее образование и перспективное будущее. И мы с удовольствием помогаем нашим ученикам на пути к этому.</p>

