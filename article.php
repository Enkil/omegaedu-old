<p style="font-size:24px; margin-bottom:5px;">«Система Физтеха». Все о лучшем техническом вузе страны.</p>

<p style="color:#777; font-size:14px;"><i style="font-size:15px;" class="fa fa-eye"></i> <span id="views">1274</span><br>11 января 2014</p>

<img src="http://www.examen.ru/assets/images/Articles/hazy_fizteh_00.JPG" style="width:876px; margin-bottom:10px;" />

<p>
МФТИ (Московский физико-технический институт, он же Долгопрудненский физкультурный техникум с английским уклоном, он же чаще всего подразумевается под словом Физтех) — бывший заповедник матана, гиков, задротов и прочих ебанутых. Разумеется, уже не тот, как и вообще всё образование в этой стране, но куны ещё встречаются, хотя сабж чуть более, чем наполовину наполнен мечтающими свалить в этот ваш менеджмент долбоёбами.
</p>
<p>
Когда после войны СССР очень припёрло делать Боньбу, то ВНЕЗАПНО выяснилось, что делать-то её, собственно говоря, почти некому. Инженеры ни черта не понимают в физике и прочем матане, а физики и математики не способны не то что сделать или начертить, но даже объяснить специально приставленному конструктору, куда именно надо ёбнуть кувалдой для срабатывания их теории. Конечно, с помощью НКВД Боньбу всё же построили, но на будущее озаботились подготовкой соответствующих кадров — чтоб и в матане понимали, и вид гайки в трёх проекциях не пугал. Также следует отметить довоенную историю с Капицей и освоением массового производства кислорода, которая ещё тогда показала, что для экстренного развития технологической отрасли, причём уже много лет как известной и по всему миру процветающей, оказался абсолютно необходим целый действующий академик физики, а не какие-нибудь неведомые конструкторы и менеджеры. Идею подбросили другу советских физкультурников, и в результате был создан физико-технический факультет МГУ, впоследствии, по мере выяснения полной его несовместимости с остальным универом, преобразованный в отдельный институт с преферансом и поэтессами и выселенный в тогда ещё дальние подмосковные ебеня — посёлок Долгопрудный, ныне — такой себе средней унылости примкадный раён с гопотой добровольной народной дружиной. 
</p>
<p>
Обучение в МФТИ с самого начала предполагало самостоятельную деятельность и активность. Настолько самостоятельную, что если физтеху хочется весь семестр бухать или творить другие непотребства, то ради бога, но сессию изволь сдавать исправно. Этот тихий пиздец в дальнейшем и окрестили системой физтеха, чтобы секрета враг не спиздил. Алсо, если уж студент явился на пару, то и обучение у него тоже будет особое, уличное креативное. Там, где мехматянина или физфаковца будут учить решать задачу по готовой прошивке, физтехов будут учить писать прошивки решений к задачкам. В результате, разумеется, ни одну задачу средний ботающий физтех наизусть знать не будет и не сможет (впрочем, попадаются и такие задроты), но зато сможет решать любую реально к нему попавшую.
</p>

